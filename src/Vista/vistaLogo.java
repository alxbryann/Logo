    /**
     * 
     * Metodo mediante el cual se mostraran en pantalla alertas para el usuario, o movimientos que realice el mismo.
     * @param mensaje El cual es un String que sera puesto en un label dentro del panelMoviAlertas, este mensaje puede ser o una alerta, o un movimiento
     * 
     */    
    public void a침adirMovimientos(String mensaje){
        if(mensaje.equals("eliminar")){
            panelMoviAlertas.removeAll();
            panelMoviAlertas.updateUI(); 
        }
        else{
            JLabel label = new JLabel(mensaje);
            panelMoviAlertas.add(label);
            panelMoviAlertas.updateUI();               
        }                 
    }
    /**
     * 
     * Metodo mediante el cual, se crear치 la matriz de labels que vemos en pantalla, adicionalmente a cada label, le daremos un borde y un color, en el caso de que la posicion sea [0][0], asignaremos la tortuga mirando hacia la derecha.
     * 
     */
    private void hacerMatriz() {
        int x = 10, y = 10;
        Matriz = new JLabel[filas][columnas];
        for (int i = 0; i < filas; i++) {
            for (int j = 0; j < columnas; j++) {
                Matriz[i][j] = new JLabel();
                Matriz[i][j].setBounds(x, y, 20, 20);
                if (i == 0 && j == 0) {
                    Matriz[i][j].setText("游뫀");
                    Matriz[i][j].setBorder(BorderFactory.createLineBorder(Color.black));
                    Matriz[i][j].setOpaque(true);
                    Matriz[i][j].setBackground(Color.white);
                }
                Matriz[i][j].setBorder(BorderFactory.createLineBorder(Color.black));
                Matriz[i][j].setOpaque(true);
                Matriz[i][j].setBackground(Color.white);
                panelTodo.add(Matriz[i][j]);
                x += 20;
            }
            x = 10;
            y += 20;
        }
    }

